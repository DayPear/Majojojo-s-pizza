package persistencia.dominio;

/**
 *
 * @author maria
 */
public class Empleado {
    
    int id;

    public Empleado() {
    }
    
    public Empleado(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Empleado{" + "id=" + id + '}';
    }
 
}
